"use client";

import { useState, useEffect } from "react";
import { usePosts } from '@/lib/useLocalPosts';
import type { Post } from '@/lib/types';
import styles from './blog.module.css';
import { FloatingNav } from '../components/FloatingNav';
import AdSenseDisplay from '../components/AdSenseDisplay';
import { Instagram, X, Video, Share2, Loader2 } from 'lucide-react';
import { useUser } from '@clerk/nextjs';
import { useRouter } from 'next/navigation';
import SidebarAd from '../components/SidebarAd';
import adStyles from '../components/AdStyles.module.css';

const FALLBACK_IMG = 'https://gen.pollinations.ai/image/sleek%20futuristic%20abstract%20technology%20concept%2C%20premium%20dark%20editorial%20style%2C%20glassmorphism%2C%20neon%20accents%2C%20no%20text?width=800&height=420&nologo=true&seed=42&model=flux';

export default function BlogPage() {
  const { posts, refresh } = usePosts();
  const router = useRouter();
  const { isSignedIn } = useUser();
  const [signaling, setSignaling] = useState<Record<string, boolean>>({});
  
  // Filtering state
  const [timeFilter, setTimeFilter] = useState<'all' | 'today' | 'week' | 'month'>('all');
  const [categoryFilter, setCategoryFilter] = useState<string>('all');

  useEffect(() => {
    const handleUpdate = () => refresh();
    window.addEventListener('posts-updated', handleUpdate);
    return () => window.removeEventListener('posts-updated', handleUpdate);
  }, [refresh]);

  const handleSignal = async (slug: string, platform: 'twitter' | 'instagram' | 'tiktok') => {
    try {
      setSignaling(prev => ({ ...prev, [`${slug}-${platform}`]: true }));
      const res = await fetch('/api/social', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ slug, platform })
      });
      const data = await res.json();
      if (data.success) {
        refresh();
      } else {
        alert(`Social Signal Failed: ${data.error || 'Unknown error'}`);
      }
    } catch (err) {
      console.error('Signal Error:', err);
    } finally {
      setSignaling(prev => ({ ...prev, [`${slug}-${platform}`]: false }));
    }
  };

  // Derived data
  const categories = ['all', ...Array.from(new Set(posts.map(p => p.category || 'General').filter(Boolean)))];

  const filteredPosts = posts.filter(post => {
    // 1. Time Filter
    if (timeFilter !== 'all') {
      const postDate = new Date(post.publishedAt);
      const now = new Date();
      now.setHours(0, 0, 0, 0);
      
      if (timeFilter === 'today') {
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        if (postDate < today) return false;
      } else if (timeFilter === 'week') {
        const lastWeek = new Date();
        lastWeek.setDate(lastWeek.getDate() - 7);
        if (postDate < lastWeek) return false;
      } else if (timeFilter === 'month') {
        const lastMonth = new Date();
        lastMonth.setMonth(lastMonth.getMonth() - 1);
        if (postDate < lastMonth) return false;
      }
    }

    // 2. Category Filter
    if (categoryFilter !== 'all') {
      const cat = post.category || 'General';
      if (cat !== categoryFilter) return false;
    }

    return true;
  });

  return (
    <main className={styles.blogContainer}>
      <FloatingNav />
      
      <div className={adStyles.blogLayoutWrapper}>
        <SidebarAd 
          link="https://amzn.to/41mSxQD"
          label="Today's Deals"
          image="/ads/amazon_deals_ad_sidebar.png"
        />

        <div className={adStyles.mainColumn}>
          <div className={styles.blogContent}>
            <header className={styles.header}>
              <h1>Pulse Blog</h1>
              <p>Autonomous deep-dives generated by your Niche Engine.</p>
              {isSignedIn && (
                <div className={styles.authLinks}>
                  <a href="/api/auth/tiktok/connect" className={styles.authLink}>🔗 Connect TikTok</a>
                </div>
              )}
            </header>

            {/* Filter Controls */}
            <div className={styles.filterSection}>
              <div className={styles.filterGroup}>
                <span className={styles.filterLabel}>Time Range</span>
                <div className={styles.filterOptions}>
                  {['all', 'today', 'week', 'month'].map(t => (
                    <button 
                      key={t}
                      onClick={() => setTimeFilter(t as any)}
                      className={`${styles.filterTab} ${timeFilter === t ? styles.active : ''}`}
                    >
                      {t.charAt(0).toUpperCase() + t.slice(1)}
                    </button>
                  ))}
                </div>
              </div>

              <div className={styles.filterGroup}>
                <span className={styles.filterLabel}>Category</span>
                <div className={styles.filterOptions}>
                  {categories.map(c => (
                    <button 
                      key={c}
                      onClick={() => setCategoryFilter(c)}
                      className={`${styles.filterTab} ${categoryFilter === c ? styles.active : ''}`}
                    >
                      {c}
                    </button>
                  ))}
                </div>
              </div>
            </div>

            {/* Display ad below filters */}
            <AdSenseDisplay />

            {filteredPosts.length === 0 ? (
              <div style={{ textAlign: 'center', padding: '100px 0', color: '#64748b' }}>
                <p>{posts.length === 0 ? "No pulses generated yet." : "No pulses match your filters."}</p>
              </div>
            ) : (
              <div className={styles.postGrid}>
                 {filteredPosts.map((post: Post) => (
                    <article 
                      className={styles.postCard} 
                      key={post.id} 
                      onClick={() => router.push(`/blog/${post.slug}`)}
                      style={{ cursor: 'pointer' }}
                    >
                      <div className={styles.imageWrapper}>
                        {/* eslint-disable-next-line @next/next/no-img-element */}
                        <img 
                          src={post.ogImageUrl || FALLBACK_IMG} 
                          alt={post.title}
                          style={{ width: '100%', height: '210px', objectFit: 'cover', display: 'block' }}
                          onError={(e) => { (e.target as HTMLImageElement).src = FALLBACK_IMG; }}
                        />
                      </div>
                      <div className={styles.cardContent}>
                        <div className={styles.cardHeader}>
                          <span className={styles.tag}>{post.keyword}</span>
                          {post.category && <span className={styles.categoryTag}>{post.category}</span>}
                        </div>
                        <h2 className={styles.postTitle}>{post.title}</h2>
                        <p className={styles.excerpt}>{post.metaDescription}</p>
                        <div className={styles.cardFooter}>
                          <span className={styles.date}>
                            {new Date(post.publishedAt).toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' })}
                          </span>
                          <div className={styles.socialLinks} onClick={(e) => e.stopPropagation()}>
                            {post.instagramUrl ? (
                              <a href={post.instagramUrl} target="_blank" rel="noopener noreferrer" title="View on Instagram" className={styles.socialIconActive}>
                                <Instagram size={18} />
                              </a>
                            ) : (
                              isSignedIn && (
                                <button 
                                  onClick={() => handleSignal(post.slug, 'instagram')}
                                  disabled={signaling[`${post.slug}-instagram`]}
                                  className={styles.signalButton}
                                  title="Signal to Instagram"
                                >
                                  {signaling[`${post.slug}-instagram`] ? <Loader2 size={14} className={styles.spin} /> : <Instagram size={14} />}
                                  <span>Signal</span>
                                </button>
                              )
                            )}

                            {post.twitterUrl ? (
                              <a href={post.twitterUrl} target="_blank" rel="noopener noreferrer" title="View on X" className={styles.socialIconActive}>
                                <X size={18} />
                              </a>
                            ) : (
                              isSignedIn && (
                                <button 
                                  onClick={() => handleSignal(post.slug, 'twitter')}
                                  disabled={signaling[`${post.slug}-twitter`]}
                                  className={styles.signalButton}
                                  title="Signal to X"
                                >
                                  {signaling[`${post.slug}-twitter`] ? <Loader2 size={14} className={styles.spin} /> : <X size={14} />}
                                  <span>Signal</span>
                                </button>
                              )
                            )}

                            {post.tiktokUrl ? (
                              <a href={post.tiktokUrl} target="_blank" rel="noopener noreferrer" title="View on TikTok" className={styles.socialIconActive}>
                                <Video size={18} />
                              </a>
                            ) : (
                              isSignedIn && (
                                <button 
                                  onClick={() => handleSignal(post.slug, 'tiktok')}
                                  disabled={signaling[`${post.slug}-tiktok`]}
                                  className={styles.signalButton}
                                  title="Signal to TikTok"
                                >
                                  {signaling[`${post.slug}-tiktok`] ? <Loader2 size={14} className={styles.spin} /> : <Video size={14} />}
                                  <span>Signal</span>
                                </button>
                              )
                            )}
                          </div>
                          <span className={styles.readMore}>Read Pulse →</span>
                        </div>
                      </div>
                    </article>
                ))}
              </div>
            )}

            {/* Display ad below posts grid */}
            {filteredPosts.length > 0 && <AdSenseDisplay />}
          </div>
        </div>

        <SidebarAd 
          link="https://amzn.to/4bROoJA"
          label="Flash Sale"
          image="/ads/amazon_flash_sale_ad_sidebar.png"
        />
      </div>
    </main>
  );
}
